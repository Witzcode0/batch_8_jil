#include <stdio.h>

int main() {
    // 0 - false
    // 1 - true
    
    int num1 = 10, num2 = 20;
    printf("%d\n", num1 == num2);
    printf("%d\n", num1 != num2);
    printf("%d\n", num1 < num2);
    printf("%d\n", num1 <= num2);
    printf("%d\n", num1 > num2);
    printf("%d\n", num1 >= num2);

    return 0;
}